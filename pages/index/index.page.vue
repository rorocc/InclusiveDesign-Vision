    <h1>Usability & Sehbeeinträchtigungen</h1>
    <Paragraph>Die Bedürfnisse von Nutzenden mit Sehstörungen unterscheiden sich individuell, abhängig von der Art der Einschränkung und der Schwere der Einschränkung. Um darauf eingehen zu können, kann man diese Bedürfnisse in verschiedene Kategorien einteilen.
      Um Webseiten und Software so barrierefrei wie möglich zu gestalten sollte den Nutzenden ermöglicht werden, eigene Einstellungen in eben diesen Kategorien treffen zu können um ihr Nutzungserlebnis nach eigenen Wünschen anpassen zu können.
    </Paragraph>

    <h2>Helligkeit & Farbe</h2>
    <Paragraph>Helles Licht eines Bildschirmes oder von anderen Quellen erschwert es einigen Menschen mit schlechter Sicht Texte zu lesen und kann auch Schmerzen verursachen. Dies kann durch eine Einstellungsmöglichkeit der Helligkeit vermieden werden.
      Auch Hintergrund- und Schriftfarbe zur Anpassung des Kontrastes spielen eine wichtige Rolle für die Lesbarkeit. Hierbei können die optimalen Bedingungen für die Lesbarkeit stark zwischen individuellen Menschen unterscheiden, weswegen eine breite Möglichkeit an Einstellungen hilft.
      Weiterhin können durch Farbsinnstörungen Einschränkungen beim Betrachten entstehen, weswegen es für eine Barrierefreiheit wichtig ist, nicht auf Farben zurückzugreifen um verschiedene Elemente zu unterscheiden.
    </Paragraph>

    <h2>Wahrnehmung</h2>
    <Paragraph>Bei der Wahrnehmung geht um das Erkennen von einzelnen Zeichen und Symbolen. Dabei können verschiedene Elemente verwendet werden, um Text und Anzeigen für Nutzende lesbarer zu gestalten.
      Dies kann durch das freie Einstellen von Schriftgröße, dem Schriftfont, dem Schriftstil (z.B. fett oder unterstrichen) oder von Groß- und Kleinschreibung ermöglicht werden.
    </Paragraph>

    <h2>Text- und Zeilenabstand zum Lesen</h2>
    <Paragraph>Mithilfe von variabel einstellbaren Abständen zwischen Zeilen, Buchstaben und Wörtern können Texte lesbarer gestaltet werden. Auch eine freie Einstellung der Bündigkeit kann dabei unterstützen.
      Weiterhin ist für einige Menschen schwierig Buchstaben zu unterscheiden wenn diese nah am Rand sind. Durch frei einstellbare Seitenränder und/oder einstellbare Umrandungen um einzelne Textblöcke zu unterscheiden kann die Lesbarkeit weiterhin gesteigert werden.
    </Paragraph>

    <h2>Steuern von individuellen Elementen</h2>
    <Paragraph>Durch das Verändern von Schriftgröße kann es dazu kommen, dass gewisse Elemente wie bspw. Überschriften zu groß werden um den eigentlichen Text gut lesen zu können. Weiterhin präferieren einige Menschen es, den Haupttext größer einzustellen als Überschriften oder haben einen Stil gefunden, mit dem sie persönlich die beste Lesbarkeit haben. Deswegen kann es helfen, Nutzenden zu erlauben einzelne Elemente wie eben Überschriften, Text oder Listen individuell anzupassen. </Paragraph>

    <h2>Betrachtungsfeld und Nähe</h2>
    <Paragraph>Durch das Vergrößern von Text ändert sich auch der Bereich der im Sichtfeld ist. Wenn der Fokuspunkt dabei nicht der gleiche bleibt wie vor dem Verändern kann es für einige Menschen schwierig sein, diesen Punkt wiederzufinden. Deswegen sollte der Fokuspunkt bei Anpassungen wie Zoomen oder Formatieren der gleiche bleiben.
      Menschen mit einem limitierten Sichtfeld haben wenige Elemente zur gleichen Zeit in ihrem Sichtfeld. Wenn zusammengehörende Informationen oder Feedback nach Aktionen nicht in der Nähe zueinander sind, kann es schwierig werden diese wahrzunehmen.
    </Paragraph>

    <h2>Einstellungsmöglichkeiten richtig einbinden</h2>
    <Paragraph>Durch das Verändern von bspw. Größe oder Zeilenabstand kann es passieren, dass Elemente aus dem Sichtfeld verschwinden oder auch Überlappen und unlesbar werden. Deswegen ist es wichtig, eine Größenanpassung so zu gestalten, dass alle Elemente in der richtigen Form bleiben und keine Interface-Elemente versteckt werden, die der Nutzende sehen sollte.
      Da es weiterhin für einige Nutzende schwierig ist, Text auf einem Bildschirm zu lesen sollte es ermöglicht werden, den Text jederzeit druckbar zu machen und auch die individuellen Einstellungen die getroffen worden sind zu berücksichtigen.
      Die meisten Betriebssysteme oder Browser ermöglichen Nutzenden es, Standarteinstellungen nach ihrer persönlichen Präferenz zu treffen. Diese sollten von einer barrierefreien Website automatisch übernommen werden können.
